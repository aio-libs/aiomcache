import enum

STORED = b'STORED'
NOT_STORED = b'NOT_STORED'
TOUCHED = b'TOUCHED'
NOT_FOUND = b'NOT_FOUND'
DELETED = b'DELETED'
VERSION = b'VERSION'
EXISTS = b'EXISTS'
OK = b'OK'

class StatusCode(enum.IntEnum):
    NO_ERROR = 0x0000
    KEY_NOT_FOUND = 0x0001
    KEY_EXISTS = 0x0002
    VALUE_TOO_LARGE = 0x0003
    INVALID_ARGUMENTS = 0x0004
    NOT_STORED = 0x0005
    INVALID_INCR = 0x0006
    UNKNOWN_COMMAND = 0x0081
    OUT_OF_MEMORY = 0x0082

class Opcode(enum.IntEnum):
    GET = 0x00
    SET = 0x01
    ADD = 0x02
    REPLACE = 0x03
    DELETE = 0x04
    INCR = 0x05
    DECR = 0x06
    QUIT = 0x07
    FLUSH = 0x08
    GETQ = 0x09
    NOOP = 0x0A
    VERSION = 0x0B
    GETK = 0x0C
    GETKQ = 0x0D
    APPEND = 0x0E
    PREPEND = 0x0F
    STAT = 0x10
    SETQ = 0x11
    ADDQ = 0x12
    REPLACEQ = 0x13
    DELETEQ = 0x14
    INCRQ = 0x15
    DECRQ = 0x16
    QUITQ = 0x17
    FLUSHQ = 0x18
    APPENDQ = 0x19
    PREPENDQ = 0x1A
    TOUCH = 0x1C
